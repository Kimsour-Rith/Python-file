def fun_sort(lists):
    print(sorted(lists,reverse= False))


fun_sort([])
fun_sort(['Hello'])
fun_sort(['A','B','C','D','E'])
fun_sort([1,5,12,5,4])
print("*"*50)
def fun_sort_rev(lists):
    print(sorted(lists,reverse=True))


fun_sort_rev(['A','B','C','D','E'])
fun_sort_rev(['300','100','200','400'])
fun_sort_rev([1,5,12,5,4])
